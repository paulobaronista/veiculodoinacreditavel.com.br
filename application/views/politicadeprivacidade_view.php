                <p>10. ATUALIZAÇÕES E DATA DE VIGÊNCIA</p>

                <p>Periodicamente, poderemos atualizar esta política. Iremos notificá-lo acerca de mudanças
                    importantes por meio do envio de uma mensagem de e-mail para o endereço que nos forneceu
                    mais recentemente ou por meio da publicação de um aviso em destaque no nosso Serviço.
                    Nós encorajamos todos os usuários a rever esta política periodicamente para ficar a par
                    das Informações que coletamos, como as utilizamos e com quem as partilhamos.</p>

