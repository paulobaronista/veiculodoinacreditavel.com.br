                    no CNPJ/MF sob o nº 03.390.444/0002-50 ("Turner", ou "nós"). Esta política de privacidade
                    ("Política de Privacidade") explica as nossas práticas de informações online e as escolhas
                    que você pode fazer acerca da forma como as suas informações são coletadas e utilizadas por
                    este serviço digital (o "Serviço"). Referimo-nos aos seus dados pessoais como "Informações".</p>

                <p>1. QUEM É RESPONSÁVEL PELAS SUAS INFORMAÇÕES</p>

                <p>A Topsports Ventures Ltda. é a controladora dos dados relacionados com quaisquer
                    Informações coletadas pelo Serviço e por meio da sua interação com o Serviço
                    (inclusive por meio de dispositivos móveis).</p>

                <p>2. AS INFORMAÇÕES QUE COLETAMOS</p>

                <p>Recebemos Informações diretamente de você, como os dados pessoais que você nos fornece
                    para se cadastrar e contratar o Serviço e ter acesso a conteúdo do Serviço, incluindo
                    dados de identificação como nome, sobrenome, data de nascimento, CPF e gênero; dados
                    de contato, como e-mail, número de telefone celular e operadora, Estado de residência;
                    dados de pagamento, como cartão de crédito (quando aplicável); e dados sobre sua
                    preferência esportiva, como time de futebol que torce, campeonato de preferência,
                    jogador de preferência, liga, etc. Além disso, tratamos Informações que são passiva
                    ou automaticamente coletadas durante sua utilização do Serviço, como as informações
                    de utilização coletadas do seu navegador ou dispositivo, incluindo endereço de IP e
                    dados limitados de geolocalização.</p>

                <p>Informações que você nos fornece. Para se cadastrar, participar de uma atividade
                    no Serviço, como uma competição ou outra atividade semelhante, poderá ser necessário
                    que você nos forneça algumas Informações de contato, como o nome e o endereço de
                    e-mail, e outras Informações como uma fotografia ou um vídeo, dependendo da atividade
                    específica. Poderemos fornecer detalhes adicionais acerca da coleta e do tratamento
                    de tais Informações no momento em que for realizada a coleta, dependendo dos termos
                    específicos de participação nessa atividade.</p>

                <p>Informações que são passiva ou automaticamente coletadas. Também recebemos Informações
                    quando você visita o Serviço, que são automaticamente coletadas do seu navegador ou
                    dispositivo, como visualizações de páginas, cliques, endereço IP, ID do dispositivo
                    (ou equivalente), o nome do seu provedor de internet e o tipo de navegador que você
                    utiliza. Coletamos seus dados de geolocalização a partir do seu endereço de IP e do
                    GPS do seu equipamento, para finalidades limitadas conforme indicado abaixo.
                    Utilizamos cookies e outras tecnologias semelhantes para estes fins. Consulte a
                    nossa Política sobre Cookies para obter mais informações sobre como estas Informações
                    podem ser coletadas e tratadas.</p>

                <p>3. COMO UTILIZAMOS AS SUAS INFORMAÇÕES</p>

                <p>Utilizamos as suas Informações para as seguintes finalidades:</p>

                <p>3.1. Para execução de contrato ou procedimentos preliminares relacionados a contrato,
                    de acordo com o inciso V do Artigo 7º da Lei No. 13.709/18 ("LGPD"):</p>

                <p>(a) para proporcionar o Serviço, dar acesso ao conteúdo do Serviço e para nos comunicarmos
                    com você em relação à sua conta conosco quando oferecemos registro;</p>

                <p>(b) como indicado em nossos Termos e Condições de Uso, o Serviço é oferecido a usuários
                    localizados no Brasil. Não temos autorização para exibir o Conteúdo fora do território
                    brasileiro. Desse modo, para que possamos cumprir a obrigação contratual de disponibilizar
                    o conteúdo sob o Serviço, coletamos seus dados de geolocalização a partir do endereço de
                    IP e do GPS do seu equipamento, para validação da localidade e liberação do Conteúdo.
                    Tais dados serão tratados somente para autorizar a disponibilização do Conteúdo a cada
                    acesso e não serão armazenados pela Turner;</p>

                <p>(c) para lhe enviar informações sobre alterações nos nossos termos ou políticas;</p>

                <p>(d) para permitir que você participe em quaisquer competições ou promoções que realizemos
                    periodicamente, administrar a competição, notificá-lo(a) se tiver vencido, verificar a sua
                    identidade, residência e quaisquer outros requisitos de elegibilidade, conforme o caso,
                    entregar o prêmio e responder a qualquer questão que você possa ter em relação à competição;</p>

                <p>(e) para permitir que você envie Conteúdo de Usuário Final (tal como definido nos Termos de Uso),
                    e para permitir-nos usar o Conteúdo de Usuário Final que você nos enviar em conformidade com os
                    Termos de Uso ou outras condições mais específicas que possam ser aplicáveis periodicamente;</p>

                <p>3.2. Caso sejam necessárias para finalidades que sejam do nosso legítimo interesse ou do
                    legítimo interesse de terceiros, de acordo com o inciso IX do Artigo 7º da LGPD):</p>

                <p>Este interesse engloba:</p>

                <p>(a) satisfazer as suas solicitações e questões relativamente ao Serviço;</p>

                <p>(b) fornecer suporte técnico para o Serviço ou responder a uma chamada ou contato do usuário via
                    canal de atendimento, por redes sociais ou por qualquer outro meio;</p>

                <p>(c) analisar a forma como o Serviço é utilizado, incluindo solicitações de pesquisa, frequência
                    de visualização e duração do tempo de visualização, visualizações da página e cliques para nos
                    ajudar a gerir e melhorar o Serviço e desenvolver novos serviços;</p>

                <p>(d) enviar-lhe boletins informativos sobre os nossos produtos e serviços e sobre produtos e
                    serviços de terceiros autorizados, e usar tecnologias para verificar se foram recebidos e
                    abertos, a menos que você nos informe que não deseja mais receber estes boletins;</p>

                <p>(e) gerir o envio de publicidade, incluindo o número de vezes em que a mesma publicidade é
                    apresentada e visualizada;</p>

                <p>(f) aplicar as nossas políticas e os termos de uso para verificar possíveis atividades ilegais
                    (como violação de direitos de autor ou fraude);</p>

                <p>(g) realizar uma pesquisa ou um estudo de mercado sobre o Serviço de forma a compreender o
                    nosso público; e</p>

                <p>(h) proteger a segurança dos nossos usuários.</p>

                <p>3.3. Caso você nos forneça o seu consentimento de acordo com o inciso I do Artigo 7º da LGPD:</p>

                <p>(a) se você for menor de 18 (dezoito) anos de idade e quiser contratar e se cadastrar no
                    Serviço, ou participar em uma atividade que requeira o consentimento, você somente poderá
                    contratar e se cadastrar no Serviço ou participar desta atividade com o consentimento de
                    seus pais ou responsáveis para o tratamento de suas Informações, nos termos do parágrafo
                    1º do Artigo 14 da LGPD. Não tratamentos Informações de menores de 18 (dezoito) anos de
                    idade para estes tipos de atividades sem dar aos pais ou responsável a opção de autorizar
                    que os dados pessoais dos seus filhos sejam incluídos na base de dados aplicável para essa
                    atividade. Durante o período em que seja solicitado o consentimento dos pais ou responsável,
                    coletaremos Informações limitadas somente para viabilizar o pedido de consentimento, e neste
                    caso tal Informação fornecida será arquivada de forma temporária. Poderemos solicitar informações
                    adicionais dos pais ou responsáveis do menor de 18 (dezoito) anos para verificação de identidade.
                    Se, após a verificação de identidade, não recebermos o consentimento expresso de um dos pais ou
                    responsável, eliminaremos as Informações fornecidas em um período de tempo razoável. Para
                    efeitos da presente Política de Privacidade, qualquer referência a "pai" deve incluir o
                    responsável legal.</p>

                <p>3.4. Para o cumprimento de obrigação legal ou regulatória, de acordo com o inciso II do Artigo
                    7º da LGPD, e para o exercício regular de nossos direitos, de acordo com o inciso VI do Artigo 7º da LGPD:</p>

                <p>(a) em resposta a pedidos por parte do governo ou das autoridades responsáveis pela aplicação da
                    lei no decurso de uma investigação.</p>

                <p>(b) para cumprir as nossas obrigações legais ou regulatórias.</p>

                <p>(c) para o exercício ou defesa de nossos direitos em processos judiciais, administrativos e arbitrais.</p>

                <p>4. INFORMAÇÕES QUE RECEBEMOS DE TERCEIROS</p>

                <p>Quando você interage com o nosso conteúdo por meio das redes sociais ou outros sites ou
                    aplicações que não sejam da Turner, você poderá permitir que esses sites compartilhem
                    Informações conosco, como o conteúdo que você visualizou ou com o qual interagiu e os
                    anúncios inerentes ao conteúdo que lhe foram apresentados ou nos quais você clicou.</p>

                <p>As políticas de privacidades destes sites e aplicações conterão mais detalhes sobre este
                    tópico e sobre como alterar as suas definições de privacidade nesses sites e aplicações.</p>

                <p>5. COMPARTILHAMENTO E DIVULGAÇÃO DE INFORMAÇÕES</p>

                <p>Compartilhamos as suas Informações:</p>

                <p>(a) Com prestadores de serviços que contratamos para desempenharem funções relacionadas com o Serviço
                    em nosso nome. Por exemplo, para: (i) gerenciar pagamentos em relação ao Serviço; (ii) realizar
                    investigações e análises; (Iii) criar conteúdos; (iv) fornecer serviços de suporte ao cliente;
                    (v) realizar ações de marketing ou prestar apoio em marketing; (vi) atender pedidos; (vii)
                    hospedar serviços; (viii) fornecer apoio técnico ou operacional; (ix) administrar promoções,
                    competições ou outras atividades do usuário; (x) efetuar a manutenção de bases de dados; e
                    (xi) prestar suporte ao Serviço de outra forma;</p>

                <p>(b) Com operadoras de telefonia celular, caso necessário para validar o direito de um usuário
                    acessar o Serviço ou qualquer conteúdo do Serviço;</p>

                <p>(c) Em resposta a processos jurídicos, por exemplo, em resposta a uma ordem judicial ou intimação,
                    uma aplicação da lei ou um pedido de uma agência governamental;</p>

                <p>(e) Com terceiros autorizados, se isso nos ajudar a aplicar as nossas políticas e termos de uso,
                    para investigar, prevenir ou tomar medidas em relação a possíveis atividades ilegais (como violação
                    de direitos de autor ou fraude) ou proteger a segurança de outros usuários dos Serviços; ou</p>

                <p>(e) Se nós ou uma das nossas unidades comerciais passarem por uma transição comercial, como uma fusão,
                    aquisição por outra empresa ou venda dos nossos ativos na sua totalidade ou em parte.</p>

                <p>6. COOKIES E OUTRAS TECNOLOGIAS SEMELHANTES</p>

                <p>Utilizamos cookies e outras tecnologias semelhantes para ajudar a fornecer o Serviço e para analisar
                    como você e usuários autorizados usam o nosso Serviço. Também permitimos que terceiros utilizem
                    estas tecnologias para fins semelhantes. Consulte a nossa Política sobre Cookies para obter mais
                    informações.</p>

                <p>7. OUTRAS INFORMAÇÕES IMPORTANTES SOBRE A SUA PRIVACIDADE</p>

                <p>7.1. Transferência internacional</p>

                <p>Periodicamente, poderemos precisar permitir que nossos prestadores de serviços ou de suporte,
                    que pode estar localizados fora do Brasil, tenham acesso às suas Informações. Poderemos
                    igualmente realizar outras divulgações de seus dados pessoais no exterior, por exemplo,
                    se recebermos uma ordem judicial ou administrativa de uma autoridade estrangeira.</p>

                <p>Sempre tomaremos medidas para garantir que qualquer transferência internacional de informações
                    seja cuidadosamente gerida no sentido de proteger os seus direitos e interesses:</p>

                <p>(a) somente transferiremos os seus dados pessoais para países que sejam reconhecidos pelo
                    fornecimento de um nível adequado de proteção jurídica, como a Argentina, local a partir
                    do qual a nossa afiliada Imagen Satelital S.A. fornece serviços de suporte, segurança,
                    infraestrutura e manutenção dos nossos sites, ou onde consigamos garantir que estão em
                    vigor medidas alternativas para proteger os seus direitos de privacidadE.</p>

                <p>(b) as transferências para os nossos prestadores de serviços e outros terceiros autorizados
                    estarão sempre protegidas por obrigações contratuais e, se apropriado, outras garantias,
                    como cláusulas-padrão de proteção de dados, regimes de certificação, e normas corporativas
                    globais de Operador. Firmamos cláusulas-padrão de proteção de dados com a nossa afiliada
                    norte-americana Turner Broadcasting System, Inc, a qual fornece serviços de suporte,
                    segurança, infraestrutura e manutenção para os nossos sites, bem como com outras
                    empresas do Grupo WarnerMedia. Você pode encontrar uma lista com algumas das empresas
                    do grupo Warner Media e suas afiliadas <a href="https://www.warnermediaprivacy.com/affiliates/">aqui</a>. Esta lista poderá ser atualizada de tempos
                    em tempos.</p>

                <p>(c) quaisquer pedidos de informação que recebermos de autoridades ou agências reguladoras
                    serão cuidadosamente analisados antes da divulgação dos dados pessoais.</p>

                <p>Você tem o direito de nos solicitar informações adicionais sobre as salvaguardas que
                    temos implementadas, tal como mencionado acima. Entre em contato conosco conforme indicado
                    abaixo se quiser obter mais informações.</p>

                <p>7.2. Retenção das suas Informações</p>

                <p>Reteremos os seus dados pessoais durante o período razoavelmente necessário para as
                    finalidades listadas na Seção 3 desta Política de Privacidade. Em algumas circunstâncias,
                    poderemos reter os seus dados pessoais durante períodos de tempo mais longos, por exemplo,
                    se tivermos de fazê-lo de acordo com exigências legais, regulamentares, fiscais ou contábeis,
                    ou enquanto não houver decorrido um prazo prescricional para o caso de eventual litígio,
                    sempre com base na legislação aplicável. Poderemos também manter dados limitados de
                    histórico de seu relacionamento com a Turner, na medida do permitido pela legislação
                    aplicável.</p>

                <p>8. OS SEUS DIREITOS</p>

                <p>Você tem diversos direitos no que diz respeito aos seus dados pessoais. Abaixo encontram-se
                    mais informações sobre cada um desses direitos.</p>

                <p>Nos termos do artigo 18 da LGPD, você tem o direito de obter a confirmação da existência
                    da atividade de tratamento de seus dados pessoais. Caso a Turner trate seus dados
                    pessoais, você tem o direito de acesso, ou seja, de obter uma declaração simplificada
                    ou completa sobre as categorias dos dados pessoais tratados, a origem dos dados e as
                    finalidades do tratamento. Caso haja tratamento de seus dados pessoais com base no seu
                    consentimento, ou em um contrato firmado com a Turner, você também tem o direito de
                    obter cópia integral dos dados pessoais que sejam tratados com base no consentimento
                    ou no contrato, sujeito a determinadas limitações e exceções nos termos da lei. Você
                    tem o direito de solicitar a correção de dados incompletos, inexatos ou desatualizados
                    sobre você. Em determinados casos, quando os seus dados pessoais forem desnecessários,
                    excessivos ou forem tratados em desconformidade com a LGPD, você tem o direito de
                    solicitar a anonimização, bloqueio ou eliminação desses dados. Em determinados casos,
                    conforme definido e na medida do exigido pela ANPD, e sempre respeitados os segredos
                    comerciais e industriais da Turner, Você tem o direito à portabilidade dos seus dados
                    pessoais a outra empresa. Nos casos em que seus dados forem tratados com base em seu
                    consentimento, você tem o direito de solicitar a eliminação de tais dados pessoais,
                    ressalvadas as hipóteses em que a Turner tem o direito de reter os dados nos termos
                    da LGPD. Você tem o direito a obter informações sobre as entidades públicas e privadas
                    com as quais a Turner realizou uso compartilhado de seus dados pessoais. Todas as
                    vezes em que solicitarmos seu consentimento para tratar seus dados pessoais, você
                    tem o direito de recusar o consentimento. Nós sempre o(a) informaremos sobre este
                    direito, e sobre as consequências caso prefira não dar consentimento para uma
                    atividade de tratamento. Além disso, toda vez que você consentir com o tratamento
                    de seus dados pessoais para uma finalidade específica, você poderá revogar o seu
                    consentimento a qualquer tempo, ficando convalidadas todas as atividades de tratamento
                    realizadas até a data da revogação. Você tem o direito de peticionar contra a Turner
                    perante a Autoridade Nacional de Proteção de Dados - ANPD em relação aos seus dados
                    pessoais. Você tem o direito de se opor a qualquer atividade de tratamento de seus
                    dados pessoais que viole o disposto na LGPD. Você também tem o direito de solicitar
                    a revisão de decisões tomadas unicamente com base em tratamento automatizado de
                    dados pessoais que afetem seus interesses, incluídas as decisões destinadas a
                    definir o seu perfil pessoal, profissional, de consumo e de crédito ou os
                    aspectos de sua personalidade.</p>

                <p>Estes direitos poderão ser limitados, por exemplo, se a satisfação do seu pedido revelar
                    Informações sobre outra pessoa ou se nos solicitar a eliminação de Informações cuja
                    conservação é exigida por lei ou que devem ser conservadas devido a legítimos interesses prementes.</p>

                <p>Você poderá optar por não receber boletins informativos por e-mail e outras comunicações de marketing
                    seguindo as instruções de autoexclusão fornecidas nesses e-mails.</p>

                <p>Para exercer qualquer um destes direitos, você poderá entrar em contato com o nosso Departamento
                    de Privacidade tal como indicado abaixo, na Seção 9. Na solicitação, inclua as suas
                    informações de registro, se aplicável, como o primeiro nome, sobrenome e o endereço
                    de e-mail. Poderemos solicitar o fornecimento de informações adicionais para efeitos
                    de verificação de identidade.</p>

